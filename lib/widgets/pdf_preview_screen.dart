// Printer selection dialog
// ──────────────────────────────────────────────────────────────────────────────

class _PrinterSelectionDialog extends StatelessWidget {
  final List<Printer> printers;

  const _PrinterSelectionDialog({required this.printers});

  @override
  Widget build(BuildContext context) {
    return AlertDialog(
      title: const Text(
        'اختر الطابعة',
        textAlign: TextAlign.center,
        style: TextStyle(fontWeight: FontWeight.bold),
      ),
      content: SizedBox(
        width: double.maxFinite,
        child: ListView.separated(
          shrinkWrap: true,
          itemCount: printers.length,
          separatorBuilder: (_, __) => const Divider(height: 1),
          itemBuilder: (context, index) {
            final printer = printers[index];
            return ListTile(
              leading: Icon(
                printer.isDefault
                    ? Icons.local_printshop
                    : Icons.print_outlined,
                color:
                    printer.isDefault ? const Color(0xFFF58220) : Colors.grey,
              ),
              title: Text(
                printer.name,
                style: TextStyle(
                  fontWeight:
                      printer.isDefault ? FontWeight.bold : FontWeight.normal,
                ),
              ),
              subtitle: printer.isDefault
                  ? const Text(
                      'الطابعة الافتراضية',
                      style: TextStyle(
                        color: Color(0xFFF58220),
                        fontSize: 12,
                      ),
                    )
                  : null,
              onTap: () => Navigator.of(context).pop(printer),
            );
          },
        ),
      ),
      actions: [
        TextButton(
          onPressed: () => Navigator.of(context).pop(),
          child: const Text('إلغاء'),
        ),
      ],
    );
  }
}
